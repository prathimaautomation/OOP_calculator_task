# Build a basic functional
class Calculator:

# Core 1: build function containing
     # add
     def add(self, num1, num2):
         return num1 + num2

     # subtract
     def subtract(self, num1, num2):
         return num1 - num2

     # multiply
     def multiply(self, num1, num2):
         return num1 * num2

     # divide
     def divide(self, num1, num2):
         return num1 / num2

